    private void carregaCampos() {
        if(pf != null) {
            jTextField1.setText(pf.getNome());
            jTextField2.setText(pf.getCpf());
            jTextField3.setText(pf.getLogradouro() + ", " + pf.getNumero() + (pf.getComplemento() == null ? "" : pf.getComplemento()));
            jTextField4.setText(pf.getBairro());
            jTextField5.setText(pf.getCidade());
            jTextField6.setText(pf.getEstado());
            jTextField18.setText(pf.getCep());
            jTextField8.setText(pf.getNumerocel());
            jTextField9.setText(pf.getNumerowa());
            jTextField7.setText(pf.getEmail());
        }
        if(pj != null) {
            jTextField11.setText(pj.getRazaosocial());
            jTextField12.setText(pj.getFantasia());
            jTextField13.setText(pj.getCnpj());
            jTextField14.setText(pj.getLogradouro() + ", " + pj.getNumero() + (pj.getComplemento() == null ? "" : "(" + pj.getComplemento() + ")"));
            jTextField20.setText(pj.getIe());
            jTextField15.setText(pj.getBairro());
            jTextField16.setText(pj.getCidade());
            jTextField17.setText(pj.getEstado());
            jTextField19.setText(pj.getCep());
            jTextField21.setText(pj.getNumerofixo());
            jTextField22.setText(pj.getNumerocel());
            jTextField23.setText(pj.getNumerowa());
        }
    }

